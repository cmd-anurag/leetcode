int longestSubarray(vector<int>& nums) {
    int max = nums[0];
    int count = 1;
    for(int i = 1; i < nums.size(); ++i) {
        if(nums[i] > max) {
            max = nums[i];
            count = 1;
        }
        if((nums[i] == max) && (nums[i] == nums[i-1])) {
            count++;
        }
    }
    return count;
}

